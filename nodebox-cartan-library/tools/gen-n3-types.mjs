// Generate engine/n3-types.js from the official NodeBox 3 library files
// (tools/ref/*.ndbx, taken from github.com/nodebox/nodebox). This gives
// the port names, defaults, ranges, menus and descriptions of every
// standard node with zero manual transcription — the Cartan library file
// stores its port values against exactly these definitions.
//
// Run: node tools/gen-n3-types.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { parseNDBX, parsePortValue } from "./ndbx-parser.mjs";

const here = dirname(fileURLToPath(import.meta.url));
const LIBS = ["math", "list", "string", "color", "data", "corevector"];

// NodeBox 3 port/output types → unified-engine types.
const TYPE_MAP = { geometry: "shape", data: "list" };
const mapType = (t) => TYPE_MAP[t] || t;

const types = [];

for (const lib of LIBS) {
  const xml = readFileSync(join(here, "ref", `${lib}.ndbx`), "utf8");
  const doc = parseNDBX(xml);
  const ndbx = doc.children.find((c) => c.tag === "ndbx");
  const root = ndbx.children.find((c) => c.tag === "node" && c.attrs.name === "root");
  const byName = new Map();
  for (const n of root.children.filter((c) => c.tag === "node")) byName.set(n.attrs.name, n);

  // Resolve a node's full definition through its prototype chain
  // (prototypes are siblings in the same library file).
  function resolve(node) {
    const proto = node.attrs.prototype ? byName.get(node.attrs.prototype) : null;
    const base = proto
      ? resolve(proto)
      : { ports: [], outputType: "geometry", outputRange: "value", fn: null };
    const ports = base.ports.map((p) => ({ ...p }));
    for (const el of node.children.filter((c) => c.tag === "port")) {
      const a = el.attrs;
      const existing = ports.find((p) => p.name === a.name);
      const menu = el.children
        .filter((c) => c.tag === "menu")
        .map((c) => ({ key: c.attrs.key, label: c.attrs.label }));
      const def = {
        name: a.name,
        type: mapType(a.type),
        n3Type: a.type,
        range: a.range || "value",
        value: parsePortValue(a.type, a.value),
      };
      if (a.min !== undefined) def.min = parseFloat(a.min);
      if (a.max !== undefined) def.max = parseFloat(a.max);
      if (a.label) def.label = a.label;
      if (a.description) def.description = a.description;
      if (a.widget && a.widget !== "none") def.widget = a.widget;
      if (menu.length) {
        def.menu = menu;
        def.widget = "menu";
      }
      if (existing) Object.assign(existing, def);
      else ports.push(def);
    }
    return {
      ports,
      outputType: node.attrs.outputType || base.outputType,
      outputRange: node.attrs.outputRange || base.outputRange,
      fn: node.attrs.function || base.fn,
      description: node.attrs.description || "",
    };
  }

  for (const n of root.children.filter((c) => c.tag === "node")) {
    const r = resolve(n);
    types.push({
      id: `${lib}.${n.attrs.name}`,
      name: n.attrs.name,
      category: lib,
      description: r.description,
      outputType: mapType(r.outputType),
      outputRange: r.outputRange,
      fn: r.fn,
      ports: r.ports,
    });
  }
}

const header = `// GENERATED by tools/gen-n3-types.mjs — do not edit.
// Port/output metadata for the NodeBox 3 standard libraries, extracted
// from the official .ndbx library files (github.com/nodebox/nodebox,
// GPLv2). Implementations live in n3lib.js and are matched via \`fn\`.

export const N3_TYPE_DEFS = `;

writeFileSync(
  join(here, "..", "engine", "n3-types.js"),
  header + JSON.stringify(types, null, 1) + ";\n",
);
console.log(`wrote ${types.length} type defs to engine/n3-types.js`);
for (const t of types) if (!t.fn) console.log("  (no function):", t.id);
